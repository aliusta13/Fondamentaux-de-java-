    // Les noms de variables commencent par une minuscule, mais peuvent également commencer par une majuscule, bien que cela ne soit généralement pas recommandé.
    // Les noms ne doivent pas contenir de caractères spéciaux autres que le trait de soulignement (_) et le dollar ($), ni d'espace.
    // Les mots-clés utilisés par Java ne peuvent pas être utilisés comme noms de variable.
    // On utilise le camelCase, par exemple : aliUsta
    String nomDeEleve = "Ali";
    // Résumé du cours : Une variable est un nom utilisé pour stocker des données dans un espace alloué en mémoire.
    //= est l'opérateur d'assignation en Java.
    // Il y a deux types de données : les types primitifs et les types non primitifs.
    // Les types de données primitifs sont : boolean, char, byte, short, int, long, float, double
    // Java a créé les types de données non primitifs, et nous pouvons également en créer.
    // Les types primitifs ne contiennent que des valeurs, tandis que les types non primitifs contiennent à la fois des valeurs et des méthodes prédéfinies.
    // Les noms des types primitifs commencent par une lettre minuscule, tandis que ceux des types non primitifs commencent par une lettre majuscule.
    // La taille de mémoire occupée par les types primitifs est fixe, tandis que celle des types non primitifs varie en fonction de la quantité de données qu'ils contiennent.
    // Java utilise deux types de mémoire : la mémoire de pile (stack memory) et la mémoire de tas (heap memory).
