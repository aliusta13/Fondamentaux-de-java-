package Jour01_Introduction;

public class C04_TypesDonneesNonPrimitives {
    /*
                            non primitifs
    Les types de données non primitifs,
    tout comme ils sont créés par Java, peuvent également être créés par les utilisateurs.
    L'espace qu'ils occupent en mémoire n'est pas fixe et ils possèdent des méthodes spéciales.
    Par exemple, la classe String est un type de données non primitif.
    Cette classe a ses propres méthodes spéciales et
    les noms de ces méthodes commencent généralement par une majuscule.
     */
    /*
                        primitifs
    Les types de données primitifs sont créés par Java et au nombre de 8.
    Ils n'ont pas de méthodes spéciales et occupent une taille fixe en mémoire,
    leur nombre ne peut pas être augmenté.
     */
}
